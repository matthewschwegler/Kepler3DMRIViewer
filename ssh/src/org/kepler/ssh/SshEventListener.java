package org.kepler.ssh;

//////////////////////////////////////////////////////////////////////////
//// SshEventListener

/**
 * <p>
 * A SshEventListener listens for events that are issued by the ssh package.
 * </p>
 * 
 * <p>
 * Currently this class is being used in Kepler by the Provenance Recorder to be
 * notified when an ssh session is established to a remote host.
 * </p>
 * 
 * @author Norbert Podhorszki
 * @version $Id: SshEventListener.java 24234 2010-05-06 05:21:26Z welker $
 * @see SshEvent
 */
public interface SshEventListener {
	/**
	 * Report an ssh activity. This method will be called from the ssh package
	 * on important ssh activities.
	 * 
	 * @param event
	 *            The event to report.
	 */
	public void sshEvent(SshEvent event);

}